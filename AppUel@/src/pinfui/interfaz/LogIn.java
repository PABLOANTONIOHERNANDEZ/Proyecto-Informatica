    /**
     * Creacion de un hilo en segundo plano encargado de modificar la imagen de
     * los botones deslizantes
     *
     * @param imagenButtonDeslizante JLabel al que se le cambiara la imagen
     */
    private void crearHiloCambioIconButton() {
        // Creacion de un hilo para cambiar la imagen del correo cuando tengas
        // notificaciones nuevas - Asi llama mas la atencion
        new Thread(new Runnable() {
            public void run() {

                // Se controlara el tiempo para que cambie el icono cada segundo
                Calendar proximaEjecucion = Calendar.getInstance();
                proximaEjecucion.setTime(new java.util.Date());
                proximaEjecucion.add(Calendar.MILLISECOND, 5);

                int contador = 1;
                // Siempre que terminarHiloCorreo este a false se ejecutara el hilo
                while (!terminarHiloButton && contador < 19) {
                    Calendar fechaActual = Calendar.getInstance();
                    fechaActual.setTime(new java.util.Date());

                    if (fechaActual.compareTo(proximaEjecucion) >= 0) {
                        imagenButtonDeslizante.setIcon(new javax.swing.ImageIcon(
                                getClass().getResource("/resources/images/submit" + contador + ".png")));
                        contador++;
                        proximaEjecucion.setTime(new java.util.Date());
                        proximaEjecucion.add(Calendar.MILLISECOND, 5);
                    }

                    try {
                        java.lang.Thread.sleep(100);
                    } catch (Exception e) {
                    }
                }

                if (contador == 19) {
                    imagenButtonDeslizante.setIcon(
                            new javax.swing.ImageIcon(getClass().getResource("/resources/images/submit19.png")));
                }
            }
        }).start();
    }

    /**
     * Metodo encargado de cambiar las fuentes de la lista de labels que tiene
     * una pantalla
     */
    private void cambiarFuentes() {

        int size = Integer.parseInt(comboSize.getSelectedItem().toString());
        PInfUI.setSizeFuente(size);

        for (LabelDTO label : historicoFuentes) {
            label.getLabel().setFont(cambiarSize(label.getSize(), size));
            label.getLabel().setText(PInfUI.getBundle().getString(label.getIdTexto()));
        }

    }

    /**
     * Metodo especifico para cambiar el tamaño de una fuente
     *
     * @param style Tipo de estilo, negrita, cursiva, etc
     * @param sizeActual Tamaño que tiene actualmente la fuente
     * @param suma Cantidad que se quiere agrandar el tamaño de la fuente actual
     * @return Devuelve una fuente con el tamaño cambiado
     */
    private java.awt.Font cambiarSize(int sizeActual, int suma) {
        return new java.awt.Font("Tahoma", 0, sizeActual + suma);
    }

